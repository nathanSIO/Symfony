/**
 * @extends ServiceEntityRepository<Inscription>
 *
 * @method Inscription|null find($id, $lockMode = null, $lockVersion = null)
 * @method Inscription|null findOneBy(array $criteria, array $orderBy = null)
 * @method Inscription[]    findAll()
 * @method Inscription[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class InscriptionRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Inscription::class);
    }

    public function save(Inscription $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Inscription $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function chercherInscriptionEmploye($id): array
       {
           return $this->createQueryBuilder('i')
               ->andWhere('i.employe = :val')
               ->setParameter('val', $id)
               ->getQuery()
               ->getResult()
           ;
        }
        public function verificationInscription($formation): Employe
        {
            return $this->createQueryBuilder('employe')
            ->andWhere('employe.formation.libelle = :libelle')
            ->setParameter('formation', $formation)
            ->getQuery()
            ->getOneOrNullResult();
        } 


